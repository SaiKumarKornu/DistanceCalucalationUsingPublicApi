package restrospace.restrospace.Dto;


public class RestaurantDto {


	    private String restaurantNmae;

	    private String restaurantAddress;

	    private String restaurantZipcode;

	public RestaurantDto() {
	}

	public String getRestaurantNmae() {
			return restaurantNmae;
		}

		public void setRestaurantNmae(String restaurantNmae) {
			this.restaurantNmae = restaurantNmae;
		}

		public String getRestaurantAddress() {
			return restaurantAddress;
		}

		public void setRestaurantAddress(String restaurantAddress) {
			this.restaurantAddress = restaurantAddress;
		}

		public String getRestaurantZipcode() {
			return restaurantZipcode;
		}

		public void setRestaurantZipcode(String restaurantZipcode) {
			this.restaurantZipcode = restaurantZipcode;
		}

	    
}
